#include <stdio.h>

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: result of addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: result of subtaction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b:  second integer
 * Return: result of multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: result of division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: result of modulus
 */

int mod(int a, int b)
{
	return (a % b);
}
